    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText() == "") {
            JOptionPane.showMessageDialog(null, "Você deve preencher o nome da sala de café.");
            return;
        }

        if (jTextField2.getText() == "") {
            JOptionPane.showMessageDialog(null, "Você deve preencher a capacidade da sala de café.");
            return;
        }

        try {
            EspacoCafe novoEspacoCafe = new EspacoCafe();
            novoEspacoCafe.setNome(jTextField1.getText());

            int locacao = Integer.valueOf(jTextField2.getText());

            novoEspacoCafe.setLocacao(locacao);
            novoEspacoCafe.setListaPessoas(new ArrayList<Pessoa>());

            this.listaEspacoCafe.add(novoEspacoCafe);

            JOptionPane.showMessageDialog(null, "Sala de café cadastrada com sucesso!");

            Menu menu = new Menu(listaPessoas, listaSalasEvento, listaEspacoCafe);
            menu.setVisible(true);
            menu.setLocationRelativeTo(null);
            this.setVisible(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Favor informar apenas números no campo \"Capacidade\".");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

